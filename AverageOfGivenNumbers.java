public class AverageOfGivenNumbers {

	public static void main(String[] args) {
		// Write a program to print the average of below 5 numbers. 10,90.78,111,8989,7876
		
		int b=10;
		double c = 90.78; 
		int d = 111;
		int e = 8989;
		int f = 7876;
		
		double g = b+c+d+e+f;
		
		System.out.println(g/5);


	}

}
