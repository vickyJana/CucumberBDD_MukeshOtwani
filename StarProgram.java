package octO82022;

public class StarProgram {

	public static void main(String[] args) {
		// Write a program to print below pattern star pattern
		
		for(int i=0;i<=5;i++) {
			
			for(int j=0;j<=i;j++) {
				
				System.out.print("*");
				
				
			}
			
		 System.out.println();
		}

		

	}

}
